import math

pi = 3.14
x, y, z = 1, 2, 3

print(round(pi))        #Pozwala zaokrąglić liczbę do int
print(math.ceil(pi))    #zaokrągla liczbe w góre
print(math.floor(pi))   #zaokrągla liczbe w dół
print(abs(pi))          #wartość bezwzględna z liczby
print(pow(pi, 2))       #potęgi
print(math.sqrt(420))   #pierwiastek
print(max(x, y, z))     #szuka największej liczby z podanych
print(min(x, y, z))     #szuka najmniejszej liczby z podanych




